def find_misclassified_filenames(model, dataset, class_names, target_pair=None):
    """
    오분류된 이미지의 '파일명(경로)'을 찾아내는 함수
    주의: ImageFolder를 사용했다고 가정합니다 (dataset.samples에 경로가 있음)
    """
    model.eval()
    print(f"-------- 검사 시작: {target_pair if target_pair else '전체'} --------")

    # 데이터셋을 하나씩 순회 (DataLoader가 아니라 Dataset 자체를 순회해야 파일명을 알기 쉬움)
