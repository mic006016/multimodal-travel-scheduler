import math
import cv2
import numpy as np
import torch
import matplotlib.pyplot as plt
from pytorch_grad_cam import GradCAM
from pytorch_grad_cam.utils.image import show_cam_on_image

def visualize_many_gradcams(model, dataloader, class_names, target_label, num_samples=24):
    """
    특정 클래스의 정답 이미지들을 많이(num_samples) 뽑아서 그리드로 보여주는 함수
    """
    print(f"'{target_label}' 클래스 대량 시각화 중... (목표: {num_samples}장)")

    model.eval()
    target_layers = [model.features[-1]]
    cam = GradCAM(model=model, target_layers=target_layers)

    target_idx = class_names.index(target_label)

    collected_images = []

    # 1. 데이터 수집 (배치 순회)
    for inputs, labels in dataloader:
        inputs = inputs.to(device)
        labels = labels.to(device)

        outputs = model(inputs)
        _, preds = torch.max(outputs, 1)

        # 정답인 케이스만 수집
        for i in range(inputs.size(0)):
            if labels[i].item() == target_idx and preds[i].item() == target_idx:
                collected_images.append(inputs[i].cpu())

                if len(collected_images) >= num_samples:
                    break
        if len(collected_images) >= num_samples:
            break

    # 데이터가 부족할 경우 처리
    if len(collected_images) == 0:
        print("해당 클래스의 정답 샘플을 찾지 못했습니다.")
        return

    # 2. 그리드 그리기
    cols = 3  # 한 줄에 6장씩
    rows = math.ceil(len(collected_images) / cols)

    plt.figure(figsize=(10, 3.5 * rows))

    for i, img_tensor in enumerate(collected_images):
        # Grad-CAM 계산
        input_tensor = img_tensor.unsqueeze(0).to(device)
        grayscale_cam = cam(input_tensor=input_tensor, targets=None)[0, :]

        # 이미지 변환 (Denormalization)
        rgb_img = img_tensor.numpy().transpose((1, 2, 0))
        mean = np.array([0.485, 0.456, 0.406])
        std = np.array([0.229, 0.224, 0.225])
        rgb_img = std * rgb_img + mean
        rgb_img = np.clip(rgb_img, 0, 1)
        rgb_img = np.float32(rgb_img)

        # 히트맵 적용
        visualization = show_cam_on_image(rgb_img, grayscale_cam, use_rgb=True)

        ax = plt.subplot(rows, cols, i + 1)
        plt.imshow(visualization)
        plt.axis('off')

    plt.suptitle(f"Consistency Check: {target_label} ({len(collected_images)} Samples)", fontsize=16, fontweight='bold', y=1.02)
    plt.tight_layout()
    plt.show()

