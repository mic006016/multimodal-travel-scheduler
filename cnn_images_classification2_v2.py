def plot_confusion_matrix(model, dataloader):
    # 1. 모든 데이터에 대해 예측값과 정답 수집
    y_true = []
    y_pred = []

    model.eval() # 평가 모드

    print("데이터 예측 중...", end="")
    with torch.no_grad():
        for inputs, labels in dataloader:
            inputs = inputs.to(device)
            labels = labels.to(device) # CPU 연산을 위해 굳이 안 보내도 되지만 통일성 유지

            outputs = model(inputs)
            _, preds = torch.max(outputs, 1)

            # Tensor를 CPU로 가져와서 리스트에 추가
            y_true.extend(labels.cpu().numpy())
            y_pred.extend(preds.cpu().numpy())
    print(" 완료!")

    # 2. Confusion Matrix 계산
    # y_true: 실제 정답, y_pred: 모델의 예측
    cm = confusion_matrix(y_true, y_pred)

    # 3. 시각화를 위한 DataFrame 생성 (라벨 이름 포함)
    df_cm = pd.DataFrame(cm, index=class_names, columns=class_names)

    # 4. 히트맵 그리기
    plt.figure(figsize=(10, 8))
    sns.heatmap(df_cm, annot=True, fmt='d', cmap='Blues', cbar=False)
    # annot=True: 숫자 표시, fmt='d': 정수형(소수점 제거), cmap='Blues': 파란색 계열

    plt.title('Confusion Matrix (EfficientNetV2-S)', fontsize=15)
    plt.ylabel('True Label (Actual)', fontsize=12)
    plt.xlabel('Predicted Label', fontsize=12)
    plt.xticks(rotation=45) # x축 글씨 겹치지 않게 기울임
    plt.show()

