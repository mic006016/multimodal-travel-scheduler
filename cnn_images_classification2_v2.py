import cv2
from pytorch_grad_cam import GradCAM

def visualize_gradcam_unfreeze(model, num_images=4):
    # 1. 타겟 레이어 설정 (EfficientNetV2의 마지막 블록)
    # [중요] 특정 Conv2d 하나만 찝는 것보다 마지막 블록 전체를 주는 게 더 안정적입니다.
    target_layers = [model.features[-1]]

    # ★★★ [핵심 해결책] ★★★
    # 해당 레이어의 기울기 계산을 강제로 켭니다. (이거 안 하면 Grad-CAM 안 나옴)
    for param in model.features[-1].parameters():
        param.requires_grad = True

    model.eval()

    # 2. Grad-CAM 객체 생성
    cam = GradCAM(model=model, target_layers=target_layers)

    # 테스트 데이터 로드
    data_iter = iter(dataloaders['test'])
    images, labels = next(data_iter)
    images = images.to(device)

    fig, axes = plt.subplots(1, num_images, figsize=(15, 5))
    print("Grad-CAM 분석 중... (Frozen Layer 해제 완료)")

    for i in range(num_images):
        try:
            input_tensor = images[i].unsqueeze(0)

            # 3. Grad-CAM 생성
            grayscale_cam = cam(input_tensor=input_tensor, targets=None)

            # 결과가 비어있는지 체크
            if grayscale_cam is None or grayscale_cam.size == 0:
                print(f"이미지 {i}: CAM 생성 실패 (Gradient 문제)")
                continue

            grayscale_cam = grayscale_cam[0, :]

            # 4. 이미지 준비
            img = images[i].cpu().permute(1, 2, 0).numpy()
            img = img * np.array([0.229, 0.224, 0.225]) + np.array([0.485, 0.456, 0.406])
            img = np.clip(img, 0, 1)

            # 5. 수동 오버레이 (OpenCV)
            heatmap = cv2.resize(grayscale_cam, (img.shape[1], img.shape[0]))
            heatmap = np.uint8(255 * heatmap)
            heatmap = cv2.applyColorMap(heatmap, cv2.COLORMAP_JET)
            heatmap = cv2.cvtColor(heatmap, cv2.COLOR_BGR2RGB)
            heatmap = np.float32(heatmap) / 255

            visualization = 0.6 * img + 0.4 * heatmap
            visualization = np.clip(visualization, 0, 1)

            # 6. 출력
            outputs = model(input_tensor)
            _, preds = torch.max(outputs, 1)
            pred_label = class_names[preds.item()]
            true_label = class_names[labels[i].item()]

            if num_images > 1:
                ax = axes[i]
            else:
                ax = axes

            ax.imshow(visualization)
            color = 'blue' if pred_label == true_label else 'red'
            ax.set_title(f"True: {true_label}\nPred: {pred_label}", color=color)
            ax.axis('off')

        except Exception as e:
            print(f"이미지 {i} 처리 실패: {e}")
            import traceback
            traceback.print_exc()

    plt.tight_layout()
    plt.show()

# 실행
visualize_gradcam_unfreeze(model, num_images=4)

