    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model.to(device)

    count = 0

    with torch.no_grad():
        for i in range(len(dataset)):
            # 1. 데이터 가져오기
            img_tensor, label_idx = dataset[i] # (image, label)
            path = dataset.samples[i][0]       # 파일 경로 (ImageFolder 기준)

            # 2. 모델 예측
            img_input = img_tensor.unsqueeze(0).to(device) # 배치 차원 추가
            outputs = model(img_input)
            _, pred_idx = torch.max(outputs, 1)

            true_label = class_names[label_idx]
