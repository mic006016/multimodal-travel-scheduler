import torch.nn.functional as F

def show_worst_errors(model, dataloader, num_show=6):
    wrong_examples = []

    model.eval()
    with torch.no_grad():
        for inputs, labels in dataloader:
            inputs = inputs.to(device)
            labels = labels.to(device)

            outputs = model(inputs)
            probs = F.softmax(outputs, dim=1) # 확률로 변환
            max_probs, preds = torch.max(probs, 1)

            # 틀린 것만 수집
            wrong_indices = (preds != labels).nonzero(as_tuple=True)[0]

            for idx in wrong_indices:
                wrong_examples.append({
                    'img': inputs[idx].cpu(),
                    'true_label': class_names[labels[idx].item()],
                    'pred_label': class_names[preds[idx].item()],
                    'conf': max_probs[idx].item() # 확신도 (Confidence)
                })

    # 확신도(conf)가 높은 순서대로 정렬 (가장 뻔뻔하게 틀린 순)
    wrong_examples.sort(key=lambda x: x['conf'], reverse=True)

    print(f"총 오답 개수: {len(wrong_examples)}개")
    print(f"가장 확신에 차서 틀린 상위 {num_show}개를 보여줍니다.")

    # 시각화
    fig = plt.figure(figsize=(15, 10))
    for i in range(min(num_show, len(wrong_examples))):
        ex = wrong_examples[i]

        ax = plt.subplot(2, 3, i+1)
        img = ex['img'].permute(1, 2, 0).numpy()
        img = img * np.array([0.229, 0.224, 0.225]) + np.array([0.485, 0.456, 0.406])
