def visualize_specific_errors(model, dataloader, class_names, target_pairs):
    """
    특정 오분류 케이스를 모두 찾아서 파일명과 함께 시각화하는 함수
    """
    model.eval()
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model.to(device)

    # 데이터셋 원본 (파일 경로 접근용)
    dataset = dataloader.dataset

    # 수집된 오분류 데이터를 담을 리스트
    collected_errors = []

    print(f"분석 시작... (Target: {target_pairs})")

    # 배치 단위가 아니라 인덱스로 접근 (파일명 매칭을 정확히 하기 위해)
    current_idx = 0

    with torch.no_grad():
        for inputs, labels in dataloader:
            inputs = inputs.to(device)
            labels = labels.to(device)

            outputs = model(inputs)
            _, preds = torch.max(outputs, 1)

            batch_size = inputs.size(0)

            # 틀린 것 찾기
            not_matches = (preds != labels)

            # 틀린 인덱스(배치 내부) 추출
            error_batch_indices = np.where(not_matches.cpu().numpy())[0]

            for local_idx in error_batch_indices:
                global_idx = current_idx + local_idx

                true_idx = labels[local_idx].item()
                pred_idx = preds[local_idx].item()

                true_label = class_names[true_idx]
                pred_label = class_names[pred_idx]

                # 우리가 원하는 타겟 쌍인지 확인
                if (true_label, pred_label) in target_pairs:

                    # 파일 경로 가져오기 (ImageFolder 사용 가정)
                    # valid loader는 shuffle=False여야 정확한 매칭이 됩니다.
                    full_path = dataset.samples[global_idx][0]
                    filename = os.path.basename(full_path)

                    # 이미지 데이터 준비 (시각화용)
                    img_tensor = inputs[local_idx].cpu()

                    collected_errors.append({
                        'img_tensor': img_tensor,
                        'true': true_label,
                        'pred': pred_label,
                        'filename': filename
                    })

            current_idx += batch_size

    print(f"총 {len(collected_errors)}장의 오분류 이미지를 발견했습니다.")
    return collected_errors

