            if target_pair:
                target_true, target_pred = target_pair
                if true_label != target_true or pred_label != target_pred:
                    continue
            else:
                # 타겟 없으면 그냥 맞춘건 패스
                if true_label == pred_label:
                    continue

            # 4. 결과 출력
            print(f"[검출] 실제: {true_label} | 예측: {pred_label} | 파일경로: {path}")
